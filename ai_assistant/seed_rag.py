    """Seed RAG knowledge base with default documents."""
    async with AsyncSessionLocal() as db:
        for doc_data in RAG_DOCUMENTS:
            exists = await db.scalar(
                select(AIRAGDocument.id).where(AIRAGDocument.title == doc_data["title"])
            )
